        sdf.format(new Date());
        
        PreparedStatement ps;
        ResultSet rs;
        
        surname = jTextField2.getText();
        name = jTextField1.getText();
        patronymic = jTextField3.getText();
        position = jTextField4.getText();
        salary = jTextField6.getText();
        date_of_birth = parseDate(jDateChooser1.getDate());
        date_of_start = parseDate(jDateChooser2.getDate());
        phone = jTextField8.getText();
        city = jTextField9.getText();
        street = jTextField10.getText();
        zip = jTextField11.getText();
        try {
        password = coder.encrypt(jTextField12.getText());
        } catch (Exception e){
            e.printStackTrace();
        }
        
